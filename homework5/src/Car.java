package homework5;

public class Car {

    int id;
    String brand;
    String model;
    int yearManufacture;
    String color;
    double price;
    String regNumber;

    public Car(int id, String brand, String model, int yearManufacture, String color, double price, String regNumber) {
        this.id = id;
        this.brand = brand;
        this.model = model;
        this.yearManufacture = yearManufacture;
        this.color = color;
        this.price = price;
        this.regNumber = regNumber;
    }
}
